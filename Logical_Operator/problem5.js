// Given the days of the week in short format "Sun", "Mon" ... print in long format "Sunday", "Monday", ...

let day= "fri";

day=="sun" ? console.log("Sunday") 
  : day=="mon" ? console.log("Monday")
   : day=="tue" ? console.log("Tuesday")
    : day=="wed" ? console.log("Wednesday")
      : day=="thur" ? console.log("Thurday")
        : day=="fri" ? console.log("Friday")
          : day=="sat" ? console.log("Saturday")
            : console.log("Invalid Keywords")
